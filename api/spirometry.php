                </p>
                <p class="overview-text">
                    The test takes only 10-15 minutes and provides immediate results. You'll breathe into a mouthpiece connected to a spirometer, which measures your lung capacity (FVC) and how quickly you can exhale (FEV1). These measurements help doctors assess lung health and monitor treatment effectiveness.
                </p>
                <p class="overview-text">
                    Our digital spirometers provide accurate, computerized measurements with instant printouts. Regular spirometry testing helps track disease progression and adjust medications for optimal respiratory health.
